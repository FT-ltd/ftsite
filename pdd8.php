                поворота соответствующего направления, а если они отсутствуют или неисправны - рукой. 
                При выполнении маневра не должны создаваться опасность для движения, а также помехи 
                другим участникам дорожного движения. <br>
                Сигналу левого поворота (разворота) соответствует вытянутая в сторону левая рука либо 
                правая, вытянутая в сторону и согнутая в локте под прямым углом вверх. Сигналу правого 
                поворота соответствует вытянутая в сторону правая рука либо левая, вытянутая в сторону и 
                согнутая в локте под прямым углом вверх. Сигнал торможения подается поднятой вверх левой 
                или правой рукой.
              </p>
              <p>
                <b class="numStyle">8.2.</b> Подача сигнала указателями поворота или рукой должна производиться 
                заблаговременно до начала выполнения маневра и прекращаться немедленно после его завершения 
                (подача сигнала рукой может быть закончена непосредственно перед выполнением маневра). 
                При этом сигнал не должен вводить в заблуждение других участников движения. <br>
                Подача сигнала не дает водителю преимущества и не освобождает его от принятия мер предосторожности.
              </p>
              <p>
                <b class="numStyle">8.3.</b> При выезде на дорогу с прилегающей территории водитель должен уступить 
                дорогу транспортным средствам и пешеходам, движущимся по ней, а при съезде с дороги - пешеходам и 
                велосипедистам, путь движения которых он пересекает.
              </p>
              <p>
                <b class="numStyle">8.4.</b> При перестроении водитель должен уступить дорогу транспортным средствам, 
                движущимся попутно без изменения направления движения. При одновременном перестроении транспортных средств, 
                движущихся попутно, водитель должен уступить дорогу транспортному средству, находящемуся справа.
              </p>
              <p>
                <b class="numStyle">8.5.</b> Перед поворотом направо, налево или разворотом водитель обязан заблаговременно 
                занять соответствующее крайнее положение на проезжей части, предназначенной для движения в данном направлении, 
                кроме случаев, когда совершается поворот при въезде на перекресток, где организовано круговое движение. <br> 
                При наличии слева трамвайных путей попутного направления, расположенных на одном уровне с проезжей частью, 
                поворот налево и разворот должны выполняться с них, если знаками 5.15.1 <img class="textImg" src="#"> или 5.15.2 
