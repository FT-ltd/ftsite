              </p>
              <p>
                <b class="numStyle">8.6.</b> Поворот должен осуществляться таким образом, чтобы при выезде с пересечения проезжих 
                частей транспортное средство не оказалось на стороне встречного движения. <br>
                При повороте направо транспортное средство должно двигаться по возможности ближе к правому краю проезжей части.
              </p>
              <p>
                <b class="numStyle">8.7.</b> Если транспортное средство из-за своих габаритов или по другим причинам не может 
                выполнить поворот с соблюдением требований пункта 8.5 Правил, допускается отступать от них при условии 
                обеспечения безопасности движения и если это не создаст помех другим транспортным средствам.
              </p>
              <p>
                <b class="numStyle">8.8.</b> При повороте налево или развороте вне перекрестка водитель безрельсового транспортного 
                средства обязан уступить дорогу встречным транспортным средствам и трамваю попутного направления. <br>
                Если при развороте вне перекрестка ширина проезжей части недостаточна для выполнения маневра из крайнего левого положения, 
                его допускается производить от правого края проезжей части (с правой обочины). При этом водитель должен уступить 
                дорогу попутным и встречным транспортным средствам.
              </p>
              <p>
                <b class="numStyle">8.9.</b> В случаях, когда траектории движения транспортных средств пересекаются, 
