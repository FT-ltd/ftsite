            <h5 class=" text-center tc-Teal2">13. Проезд перекрестков</h5>
            <div class="textPdd">
              <p>
                <b class="numStyle">13.1.</b> При повороте направо или налево водитель обязан уступить 
                дорогу пешеходам и велосипедистам, пересекающим проезжую часть дороги, на которую он поворачивает.
              </p>
              <p>
                <b class="numStyle">13.2.</b>  Запрещается выезжать на перекресток, пересечение проезжих 
                частей или участка перекрестка, обозначенного разметкой 1.26 <img class="textImg" src="#">, если 
                впереди по пути следования образовался затор, который вынудит водителя остановиться, создав 
                препятствие для движения транспортных средств в поперечном направлении, за исключением поворота 
                направо или налево в случаях, установленных настоящими Правилами.
              </p>
              <p>
                <b class="numStyle">13.3.</b> Перекресток, где очередность движения определяется сигналами 
                светофора или регулировщика, считается регулируемым. <br>
                При желтом мигающем сигнале, неработающих светофорах или отсутствии регулировщика перекресток 
                считается нерегулируемым, и водители обязаны руководствоваться правилами проезда нерегулируемых 
                перекрестков и установленными на перекрестке знаками приоритета.
              </p>
              <p>
              <h5 class=" text-center tc-Teal2">Регулируемые перекрестки</h5>
              </p>
              <p>
                <b class="numStyle">13.4.</b> При повороте налево или развороте по зеленому сигналу светофора 
                водитель безрельсового транспортного средства обязан уступить дорогу транспортным средствам, 
                движущимся со встречного направления прямо или направо. Таким же правилом должны руководствоваться 
                между собой водители трамваев.
              </p>
              <p>
                <b class="numStyle">13.5.</b> При движении в направлении стрелки, включенной в дополнительной 
                секции одновременно с желтым или красным сигналом светофора, водитель обязан уступить дорогу 
                транспортным средствам, движущимся с других направлений.
              </p>
              <p>
                <b class="numStyle">13.6.</b> Если сигналы светофора или регулировщика разрешают движение одновременно 
                трамваю и безрельсовым транспортным средствам, то трамвай имеет преимущество независимо от направления 
                его движения. Однако при движении в направлении стрелки, включенной в дополнительной секции одновременно 
                с красным или желтым сигналом светофора, трамвай должен уступить дорогу транспортным средствам, движущимся 
                с других направлений.
              </p>
              <p>
                <b class="numStyle">13.7.</b> Водитель, въехавший на перекресток при разрешающем сигнале светофора, должен 
                выехать в намеченном направлении независимо от сигналов светофора на выходе с перекрестка. Однако, 
                если на перекрестке перед светофорами, расположенными на пути следования водителя, имеются стоп-линии 
                (знаки 6.16 <img class="textImg" src="#">), водитель обязан руководствоваться сигналами каждого светофора.
              </p>
              <p>
                <b class="numStyle">13.8.</b>  При включении разрешающего сигнала светофора водитель обязан уступить дорогу 
                транспортным средствам, завершающим движение через перекресток, и пешеходам, не закончившим переход проезжей 
                части данного направления.
              </p>
              <h5 class=" text-center tc-Teal2">Нерегулируемые перекрестки</h5>
              <p>
                <b class="numStyle">13.9.</b> На перекрестке неравнозначных дорог водитель транспортного средства, 
                движущегося по второстепенной дороге, должен уступить дорогу транспортным средствам, приближающимся 
                по главной, независимо от направления их дальнейшего движения. <br>
                На таких перекрестках трамвай имеет преимущество перед безрельсовыми транспортными средствами, 
                движущимися в попутном или встречном направлении по равнозначной дороге, независимо от направления его движения.
              </p>
              <p>
                <b class="numStyle">13.10.</b> В случае, когда главная дорога на перекрестке меняет направление, водители, движущиеся 
                по главной дороге, должны руководствоваться между собой правилами проезда перекрестков равнозначных дорог. 
                Этими же правилами должны руководствоваться водители, движущиеся по второстепенным дорогам.
              </p>
              <p>
                <b class="numStyle">13.11.</b> На перекрестке равнозначных дорог, за исключением случая, предусмотренного 
                пунктом 13.11(1) Правил, водитель безрельсового транспортного средства обязан уступить дорогу транспортным 
                средствам, приближающимся справа. Этим же правилом должны руководствоваться между собой водители трамваев. <br>
                На таких перекрестках трамвай имеет преимущество перед безрельсовыми транспортными средствами независимо от 
                направления его движения.
              </p>
              <p>
                <b class="numStyle">13.11(1).</b> При въезде на перекресток, на котором организовано круговое движение и который 
                обозначен знаком 4.3 <img class="textImg" src="#">, водитель транспортного средства обязан уступить дорогу 
                транспортным средствам, движущимся по такому перекрестку.
              </p>
              <p>
                <b class="numStyle">13.12.</b> При повороте налево или развороте водитель безрельсового транспортного средства 
                обязан уступить дорогу транспортным средствам, движущимся по равнозначной дороге со встречного направления прямо 
                или направо. Этим же правилом должны руководствоваться между собой водители трамваев.
              </p>
              <p>
                <b class="numStyle">13.13.</b> Если водитель не может определить наличие покрытия на дороге (темное время суток, 
                грязь, снег и тому подобное), а знаков приоритета нет, он должен считать, что находится на второстепенной дороге.
              </p>
