              При возникновении в пути прочих неисправностей, с которыми приложением к Основным положениям запрещена эксплуатация транспортных средств, 
              водитель должен устранить их, а если это невозможно, то он может следовать к месту стоянки или ремонта с соблюдением необходимых мер 
              предосторожности; <br> <br>
              ** В дальнейшем - Основные положения.
            </p>
            <p>
              <b class="numStyle">2.3.2.</b>  По требованию должностных лиц, уполномоченных на осуществление федерального государственного надзора в области 
              безопасности дорожного движения, проходить освидетельствование на состояние алкогольного опьянения и медицинское освидетельствование на состояние 
              опьянения. Водитель транспортного средства Вооруженных Сил Российской Федерации, Федеральной службы войск национальной гвардии Российской Федерации, 
