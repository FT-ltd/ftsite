              либо в условиях ограниченной видимости при нахождении на проезжей части или обочине быть одетым в куртку, жилет или жилет-накидку с полосами световозвращающего материала, 
              соответствующих требованиям ГОСТа 12.4.281-2014.
            </p>
            <p>
              <b class="numStyle">2.4.</b> Право остановки транспортных средств предоставлено регулировщикам, а также:
              <ul>
                <li>уполномоченным должностным лицам Федеральной службы по надзору в сфере транспорта в отношении остановки грузовых автомобилей и автобусов в специально обозначенных 
                дорожным знаком 7.14 <img class="textImg" src="#"> пунктах транспортного контроля;</li>
                <li>уполномоченным должностным лицам таможенных органов в отношении остановки транспортных средств, в том числе не осуществляющих международные перевозки товаров, 
