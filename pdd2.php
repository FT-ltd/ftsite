                составляет 3,5 тонны и более, также на иных территориях Российской Федерации, определенных законодательством Российской Федерации о таможенном регулировании, 
                в местах, специально обозначенных дорожным знаком 7.14.1 <img class="textImg" src="#">.</li>
              </ul>
