            </p>
            <p>
              <b class="numStyle">2.5.</b> При дорожно-транспортном происшествии водитель, причастный к нему, обязан немедленно остановить (не трогать с места) транспортное средство, включить 
              аварийную сигнализацию и выставить знак аварийной остановки в соответствии с требованиями пункта 7.2 Правил, не перемещать предметы, имеющие отношение к происшествию. 
              При нахождении на проезжей части водитель обязан соблюдать меры предосторожности.
            </p>
            <p>
              <b class="numStyle">2.6.</b> Если в результате дорожно-транспортного происшествия погибли или ранены люди, водитель, причастный к нему, обязан:
              <ul>
                <li>принять меры для оказания первой помощи пострадавшим, вызвать скорую медицинскую помощь и полицию;</li>
                <li>в экстренных случаях отправить пострадавших на попутном, а если это невозможно, доставить на своем транспортном средстве в ближайшую медицинскую организацию, сообщить 
                свою фамилию, регистрационный знак транспортного средства (с предъявлением документа, удостоверяющего личность, или водительского удостоверения и регистрационного документа 
                на транспортное средство) и возвратиться к месту происшествия;</li>
                <li>освободить проезжую часть, если движение других транспортных средств невозможно, предварительно зафиксировав, в том числе средствами фотосъемки или видеозаписи, 
                положение транспортных средств по отношению друг к другу и объектам дорожной инфраструктуры, следы и предметы, относящиеся к происшествию, и принять все возможные 
                меры к их сохранению и организации объезда места происшествия;</li>
                <li>записать фамилии и адреса очевидцев и ожидать прибытия сотрудников полиции.</li>
              </ul>
            </p>
            <p>
              <b class="numStyle">2.6.1.</b> Если в результате дорожно-транспортного происшествия вред причинен только имуществу, водитель, причастный к нему, обязан освободить 
              проезжую часть, если движению других транспортных средств создается препятствие, предварительно зафиксировав любыми возможными способами, в том числе средствами 
              фотосъемки или видеозаписи, положение транспортных средств по отношению друг к другу и объектам дорожной инфраструктуры, следы и предметы, относящиеся к происшествию, 
              и повреждения транспортных средств. <br>
              Водители, причастные к такому дорожно-транспортному происшествию, не обязаны сообщать о случившемся в полицию и могут оставить место дорожно-транспортного 
              происшествия, если в соответствии с законодательством об обязательном страховании гражданской ответственности владельцев транспортных средств оформление 
              документов о дорожно-транспортном происшествии может осуществляться без участия уполномоченных на то сотрудников полиции. <br> <br>
              Если в соответствии с законодательством об обязательном страховании гражданской ответственности владельцев транспортных средств документы о дорожно-транспортном 
              происшествии не могут быть оформлены без участия уполномоченных на то сотрудников полиции, водитель, причастный к нему, обязан записать фамилии и адреса очевидцев 
              и сообщить о случившемся в полицию для получения указаний сотрудника полиции о месте оформления дорожно-транспортного происшествия.
            </p>
            <p>
              <b class="numStyle">2.7.</b> Водителю запрещается:
              <ul>
                <li>управлять транспортным средством в состоянии опьянения (алкогольного, наркотического или иного), под воздействием лекарственных препаратов, ухудшающих реакцию 
                и внимание, в болезненном или утомленном состоянии, ставящем под угрозу безопасность движения;</li>
                <li>передавать управление транспортным средством лицам, находящимся в состоянии опьянения, под воздействием лекарственных препаратов, в болезненном или утомленном 
                состоянии, а также лицам, не имеющим при себе водительского удостоверения на право управления транспортным средством соответствующей категории или подкатегории, 
