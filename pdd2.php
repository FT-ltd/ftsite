              не перевозить пассажиров, не пристегнутых ремнями. При управлении мотоциклом быть в застегнутом мотошлеме и не перевозить 
              пассажиров без застегнутого мотошлема. 
            </p>
            <p>
              <b class="numStyle">2.2.</b>  Водитель механического транспортного средства, участвующий в международном дорожном движении, обязан:
              <ul>
                <li>иметь при себе и по требованию сотрудников полиции передавать им для проверки регистрационные документы на данное транспортное 
                средство (при наличии прицепа - и на прицеп) и водительское удостоверение, соответствующие Конвенции о дорожном движении, а 
                также документы, предусмотренные таможенным законодательством Евразийского экономического союза, с отметками таможенных органов, 
                подтверждающими временный ввоз данного транспортного средства (при наличии прицепа - и прицепа);</li>
                <li>иметь на данном транспортном средстве (при наличии прицепа - и на прицепе) регистрационные и отличительные знаки государства, 
                в котором оно зарегистрировано. Отличительные знаки государства могут помещаться на регистрационных знаках.</li>
              </ul>
