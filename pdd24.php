              </p>
              <p>
                <b class="numStyle">24.2.</b> Допускается движение велосипедистов в возрасте старше 14 лет: <br>
                по правому краю проезжей части - в следующих случаях:
                <ul>
                  <li>отсутствуют велосипедная и велопешеходная дорожки, полоса для велосипедистов либо отсутствует 
                  возможность двигаться по ним;</li>
                  <li>габаритная ширина велосипеда, прицепа к нему либо перевозимого груза превышает 1 м;</li>
                  <li>движение велосипедистов осуществляется в колоннах;</li>
                  <li>по обочине - в случае, если отсутствуют велосипедная и велопешеходная дорожки, полоса для 
                  велосипедистов либо отсутствует возможность двигаться по ним или по правому краю проезжей части;</li>
                </ul> 
                по тротуару или пешеходной дорожке - в следующих случаях:
                <ul>
                  <li>отсутствуют велосипедная и велопешеходная дорожки, полоса для велосипедистов либо отсутствует 
                  возможность двигаться по ним, а также по правому краю проезжей части или обочине;</li>
                  <li>велосипедист сопровождает велосипедиста в возрасте до 14 лет либо перевозит ребенка в возрасте 
                  до 7 лет на дополнительном сиденье, в велоколяске или в прицепе, предназначенном для эксплуатации 
                  с велосипедом.</li>
                </ul>
              </p>
              <p>
                <b class="numStyle">24.3.</b> Движение велосипедистов в возрасте от 7 до 14 лет должно осуществляться 
                только по тротуарам, пешеходным, велосипедным и велопешеходным дорожкам, а также в пределах пешеходных зон.
              </p>
              <p>
                <b class="numStyle">24.4.</b> Движение велосипедистов в возрасте младше 7 лет должно осуществляться только 
                по тротуарам, пешеходным и велопешеходным дорожкам (на стороне для движения пешеходов), а также в пределах 
                пешеходных зон.
              </p>
              <p>
                <b class="numStyle">24.5.</b> При движении велосипедистов по правому краю проезжей части в случаях, 
                предусмотренных настоящими Правилами, велосипедисты должны двигаться только в один ряд. <br>
                Допускается движение колонны велосипедистов в два ряда в случае, если габаритная ширина велосипедов 
                не превышает 0,75 м. <br> <br>
                Колонна велосипедистов должна быть разделена на группы по 10 велосипедистов в случае однорядного движения 
                либо на группы по 10 пар в случае двухрядного движения. Для облегчения обгона расстояние между группами 
                должно составлять 80 - 100 м.
              </p>
              <p>
                <b class="numStyle">24.6.</b> сли движение велосипедиста по тротуару, пешеходной дорожке, обочине или в 
                пределах пешеходных зон подвергает опасности или создает помехи для движения иных лиц, велосипедист 
                должен спешиться и руководствоваться требованиями, предусмотренными настоящими Правилами для движения пешеходов.
              </p>
              <p>
                <b class="numStyle">24.7.</b> Водители мопедов должны двигаться по правому краю проезжей части в один ряд 
                либо по полосе для велосипедистов. <br>
                Допускается движение водителей мопедов по обочине, если это не создает помех пешеходам.
              </p>
              <p>
                <b class="numStyle">24.8.</b> Велосипедистам и водителям мопедов запрещается:
                <ul>
                  <li>управлять велосипедом, мопедом, не держась за руль хотя бы одной рукой;</li>
                  <li>перевозить груз, который выступает более чем на 0,5 м по длине или ширине за 
                  габариты, или груз, мешающий управлению;</li>
                  <li>перевозить пассажиров, если это не предусмотрено конструкцией транспортного средства;</li>
                  <li>перевозить детей до 7 лет при отсутствии специально оборудованных для них мест;</li>
                  <li>поворачивать налево или разворачиваться на дорогах с трамвайным движением и на дорогах, 
                  имеющих более одной полосы для движения в данном направлении (кроме случаев, когда из правой 
                  полосы разрешен поворот налево, и за исключением дорог, находящихся в велосипедных зонах);</li>
                  <li>двигаться по дороге без застегнутого мотошлема (для водителей мопедов);</li>
                  <li>пересекать дорогу по пешеходным переходам.</li>
                </ul>
              </p>
              <p>
                <b class="numStyle">24.9.</b> Запрещается буксировка велосипедов и мопедов, а также буксировка 
                велосипедами и мопедами, кроме буксировки прицепа, предназначенного для эксплуатации с велосипедом или мопедом.
              </p>
              <p>
                <b class="numStyle">24.10.</b> При движении в темное время суток или в условиях недостаточной видимости 
                велосипедистам и водителям мопедов рекомендуется иметь при себе предметы со световозвращающими элементами 
                и обеспечивать видимость этих предметов водителями других транспортных средств.
              </p>
              <p>
                <b class="numStyle">24.11.</b> В велосипедной зоне:
                <ul>
                  <li>велосипедисты имеют преимущество перед механическими транспортными средствами, а 
                  также могут двигаться по всей ширине проезжей части, предназначенной для движения в 
                  данном направлении, при соблюдении требований пунктов 9.1(1) - 9.3 и 9.6 - 9.12 настоящих Правил;</li>
                  <li>пешеходам разрешается переходить проезжую часть в любом месте при условии соблюдения требований 
                  пунктов 4.4 - 4.7 настоящих Правил.</li>
                </ul>
              </p>
